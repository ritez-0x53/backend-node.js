

// Types of Middleware:

// Application-level middleware: Bound to an instance of the app using app.use() or app.METHOD().
// Router-level middleware: Bound to an instance of express.Router().
// Error-handling middleware: Defined with four parameters, used to handle errors.
// Built-in middleware: Provided by Express, such as express.json() and express.urlencoded().
// Third-party middleware: Middleware created by the community, such as morgan for logging and body-parser for parsing request bodies.

